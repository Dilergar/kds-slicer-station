-- ============================================================================
-- Миграция 019: Авто-парковка десертов — триггер через модификатор времени
-- Дата: 2026-04-23
--
-- Зачем:
--   Раньше (017) авто-парковка применялась ко ВСЕМ позициям дессертной категории.
--   На практике это срабатывало и для «несите сразу» — и десерт улетал на 40 мин
--   парковки вопреки желанию гостя.
--
--   Теперь правило: парковать ТОЛЬКО десерты у которых официант в кассе выставил
--   модификатор связанный со временем. Например:
--     * «Готовить к 18.00» — парковка до сегодняшних 18:00
--     * «Готовить к 19.00» — парковка до 19:00
--     * «Готовить позже»    — парковка на dessert_auto_park_minutes (текущие 40)
--     * «Ждать разъяснений» — аналогично, на dessert_auto_park_minutes
--
--   Без такого модификатора — десерт сразу идёт в очередь нарезчика.
--
-- Как определяем «модификатор связанный со временем»:
--   По паттернам имени из `ctlg20_modifiers.name` (ILIKE). По умолчанию:
--     ARRAY['Готовить%', 'Ждать%']
--   Админ может переопределить через UPDATE slicer_settings (без деплоя), если
--   в ресторане добавят новые модификаторы со своими именами.
--
-- Связь позиция ↔ модификатор: docm2tabl2_dishmodifiers.docm2tabl2_itemrow =
--   docm2tabl1_items.suuid. Одна позиция может иметь несколько модификаторов,
--   и правило срабатывает если хотя бы ОДИН из них совпадает с паттерном.
--
-- Чужая схема (ctlg*, docm2_*, rgst*) НЕ трогается.
-- ============================================================================

ALTER TABLE slicer_settings
  ADD COLUMN IF NOT EXISTS dessert_trigger_modifier_patterns TEXT[] NOT NULL
    DEFAULT ARRAY['Готовить%', 'Ждать%']::TEXT[];

COMMENT ON COLUMN slicer_settings.dessert_trigger_modifier_patterns IS
  'Паттерны LIKE (case-insensitive) для имён модификаторов из ctlg20_modifiers, '
  'при наличии которых дессертная позиция уходит в авто-парковку. '
  'По умолчанию: Готовить%, Ждать%. '
  'Если имя модификатора содержит точное время "Готовить к HH.00" — парковка '
  'ставится до указанного часа сегодняшней даты; иначе — на '
  'dessert_auto_park_minutes минут от ordertime.';
