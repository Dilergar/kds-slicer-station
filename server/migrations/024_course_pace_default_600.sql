-- ============================================================================
-- Миграция 024: шаг курса по умолчанию 600 сек + семантика «окно уступки»
-- Дата: 2026-07-11
--
-- По итогам ревью очереди (2026-07-11) владелец зафиксировал целевую
-- семантику умной очереди «Темп курсов»:
--
--   1) «Готово» НЕ пересобирает очередь. Номер курса гостя считается по ВСЕМ
--      позициям визита, включая уже отданные нарезчиком. Реализовано в коде:
--      GET /api/orders дополнительно отдаёт visit_completed_dish_ids /
--      visit_started_at (по строкам slicer_order_state.status = 'COMPLETED'
--      открытых чеков), buildSmartQueue считает по ним курсы. Схема БД НЕ
--      меняется — все данные уже есть в slicer_order_state.
--
--   2) Шаг курса (course_pace_seconds) — это «окно уступки»: на сколько
--      секунд курс N стола уступает дорогу первым курсам гостей, пришедших
--      позже. Виртуальное время позиции = старт визита + номер_курса × шаг;
--      до его наступления первые блюда новых столов проходят вперёд, после —
--      позицию уже никто не обгонит. На скорость нарезки не влияет: при
--      свободной очереди блюдо режется сразу (это только порядок при
--      конкуренции).
--
-- Прежний дефолт 120 сек не соответствовал целевой очереди владельца:
-- эталонный пример («Суп×2 столов 1,2 → Бифштекс×2 столов 2,3 → Десерт
-- стола 4 → Салат стола 1 → Бифштекс стола 1») воспроизводится движком
-- при 600 сек и ломается при 120/0 сек (проверено симуляцией на реальном
-- коде, ревью 2026-07-11). Новый дефолт — 600 сек (10 минут).
--
-- UPDATE условный: срабатывает только если стоит прежний дефолт 120 —
-- вручную выставленное владельцем значение не перезаписываем.
-- ============================================================================

ALTER TABLE slicer_settings
  ALTER COLUMN course_pace_seconds SET DEFAULT 600;

UPDATE slicer_settings
   SET course_pace_seconds = 600
 WHERE id = 1 AND course_pace_seconds = 120;

COMMENT ON COLUMN slicer_settings.course_pace_seconds IS
  'Шаг курса умной очереди («Темп курсов»), в секундах — окно уступки: '
  'виртуальное время позиции = старт визита гостя + номер_курса × шаг; до '
  'наступления этого времени первые блюда более новых гостей проходят вперёд, '
  'после — позицию никто не обгонит. Нарезку не замедляет (при свободной '
  'очереди блюдо режется сразу). По умолчанию 600 (10 минут).';
