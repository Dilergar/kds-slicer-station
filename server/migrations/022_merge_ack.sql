-- ============================================================================
-- Миграция 022: Персист merge-подтверждения виртуальных карточек Smart Wave
-- Дата: 2026-07-06
--
-- Проблема:
--   В Smart Wave несколько реальных позиций (source-заказов) одного блюда
--   собираются в одну виртуальную карточку со стеком «1 + 1 + 1». Кнопка
--   Merge («объедините их») фиксировала подтверждение ТОЛЬКО в локальном
--   React-стейте SlicerStation (`mergedSources`). Любое переключение вкладки,
--   F5 или второй планшет сбрасывали состояние: карточки снова показывали
--   «1+1» и блокировали «ГОТОВО» до повторного Merge. При этом merge
--   обычного (не виртуального) заказа персистился в БД — поведение было
--   неконсистентным.
--
-- Решение:
--   Флаг merge_ack на КАЖДОЙ позиции (slicer_order_state). Merge виртуальной
--   карточки проставляет merge_ack=TRUE всем её текущим source-заказам
--   (новый endpoint POST /api/orders/merge-ack). Рендер виртуальной карточки:
--   все подтверждённые source-ы → один объединённый блок, каждый новый
--   (merge_ack=FALSE по умолчанию) — отдельный блок → карточка показывает
--   «2 + 1» и снова требует Merge. Состояние живёт в БД → переживает F5,
--   переключение вкладок и синхронно между планшетами.
--
-- Сброс:
--   * Новая позиция → DEFAULT FALSE (не подтверждена).
--   * /restore (возврат из истории) → FALSE («чистый лист», как и остальные
--     поля состояния).
--   * Парковка/разморозка флаг НЕ трогают: подтверждение «я видел, что это
--     один батч» остаётся в силе.
-- ============================================================================

ALTER TABLE slicer_order_state
  ADD COLUMN IF NOT EXISTS merge_ack BOOLEAN NOT NULL DEFAULT FALSE;

COMMENT ON COLUMN slicer_order_state.merge_ack IS
  'Нарезчик подтвердил объединение этой позиции в виртуальной карточке Smart '
  'Wave (кнопка Merge). TRUE = позиция входит в «объединённый» блок стека '
  'виртуальной карточки; FALSE = рисуется отдельным блоком и требует Merge. '
  'Сбрасывается в FALSE при restore из истории.';
