-- ============================================================================
-- Миграция 026: звуковой сигнал при поступлении нового заказа
-- Дата: 2026-07-12
--
-- До этой миграции единственным звуком модуля был сигнал готовности
-- разморозки (миграция 016). Владелец попросил отдельный сигнал на
-- ПОЯВЛЕНИЕ нового заказа на доске: в шумной кухне нарезчик не смотрит
-- в планшет постоянно и мог замечать новые позиции с опозданием.
--
-- Колонка — глобальный тумблер звука (правится в UI: Админка → Общие
-- Настройки → «Звук при новом заказе»). Сама логика живёт на фронте
-- (SlicerStation): поллинг GET /api/orders каждые 4 сек, появление ранее
-- не виденного order_item_id => двойной beep (playNewOrderBeep, utils.ts).
-- Паттерн звука намеренно отличается от 3-тонального сигнала разморозки,
-- чтобы события различались на слух.
--
-- По умолчанию ВКЛ — симметрично enable_defrost_sound (миграция 016).
-- ============================================================================

ALTER TABLE slicer_settings
  ADD COLUMN IF NOT EXISTS enable_new_order_sound BOOLEAN NOT NULL DEFAULT TRUE;
