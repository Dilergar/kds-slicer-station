-- ============================================================================
-- Миграция 023: «Шаг курса» для умной очереди v2 («Темп курсов»)
-- Дата: 2026-07-07
--
-- Умная очередь переработана (дизайн владельца): вместо волновой симуляции
-- с FIFO-корзинами — модель «Темп курсов»:
--   * каждый гость (стол / чек доставки) движется по своим курсам независимо;
--   * виртуальное время позиции = время первого заказа гостя
--       + (номер курса − 1) × course_pace_seconds;
--   * очередь сортируется по виртуальному времени → столы с короткими
--     заказами (один десерт) не ждут, пока большие столы пройдут все курсы,
--     а большие столы не голодают из-за потока новых маленьких;
--   * одинаковые блюда вливаются в карточку выше, только если это не даёт
--     гостю курс N раньше курса N−1.
--
-- course_pace_seconds = «сколько в среднем гость ест одно блюдо».
--   Больше  → столы обслуживаются «тактами», короткие заказы выходят раньше.
--   Меньше  → очередь ближе к строгому FIFO по времени заказа.
-- Правится в UI: Админка → Общие настройки → «Волновая Агрегация (Умная)».
-- ============================================================================

ALTER TABLE slicer_settings
  ADD COLUMN IF NOT EXISTS course_pace_seconds INT NOT NULL DEFAULT 120;

COMMENT ON COLUMN slicer_settings.course_pace_seconds IS
  'Шаг курса умной очереди v2 («Темп курсов»), в секундах: сколько в среднем '
  'гость ест одно блюдо. Виртуальное время позиции = время первого заказа '
  'гостя + (номер курса − 1) × шаг. Большие значения → обслуживание тактами '
  '(короткие заказы выходят раньше), маленькие → ближе к строгому FIFO.';
