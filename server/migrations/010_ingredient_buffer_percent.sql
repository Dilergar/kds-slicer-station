-- ============================================================================
-- Миграция 010: slicer_ingredients.buffer_percent
-- Дата: 2026-04-19
--
-- Dashboard → «Расход Ингредиентов» показывает колонку «+ %» (надбавка
-- на потери/чистку/брутто). Раньше процент хранился только в React state
-- компонента IngredientUsageSection — при F5 терялся и возвращался к 0.
-- Теперь персистим на уровне ингредиента: каждый child ингредиент имеет
-- свой процент, родитель показывает агрегированный производный.
-- ============================================================================

ALTER TABLE slicer_ingredients
  ADD COLUMN IF NOT EXISTS buffer_percent NUMERIC(5, 2) NOT NULL DEFAULT 0;

COMMENT ON COLUMN slicer_ingredients.buffer_percent IS
  'Надбавка в процентах для расчёта брутто в отчёте «Расход Ингредиентов» '
  '(Dashboard). 0 = брутто равно нетто. Типичные значения 5–25%. '
  'Хранится на уровне каждого ингредиента (включая child-разновидности) '
  'чтобы нарезчик мог задать разный процент для сырых/приготовленных форм.';
